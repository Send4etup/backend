def create_connection(db_file):
    """–°–æ–∑–¥–∞–Ω–∏–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ SQLite –ë–î"""
    try:
        conn = sqlite3.connect(db_file)
        print(f"‚úÖ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –ë–î {db_file} —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ")
        return conn
    except sqlite3.Error as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î: {e}")
        return None


def insert_task(conn, subject_id, exam_type, task_data):
    """
    –í—Å—Ç–∞–≤–∫–∞ –∑–∞–¥–∞–Ω–∏—è –≤ —Ç–∞–±–ª–∏—Ü—É exam_tasks

    Parameters:
    - conn: –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –ë–î
    - subject_id: ID –ø—Ä–µ–¥–º–µ—Ç–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, '–º–∞—Ç–µ–º–∞—Ç–∏–∫–∞')
    - exam_type: —Ç–∏–ø —ç–∫–∑–∞–º–µ–Ω–∞ ('–û–ì–≠' –∏–ª–∏ '–ï–ì–≠')
    - task_data: —Å–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏ –∑–∞–¥–∞–Ω–∏—è
    """
    sql = '''
          INSERT INTO exam_tasks (subject_id, exam_type, task_number, difficulty, \
                                  question_text, answer_type, answer_options, \
                                  correct_answer, explanation, points, \
                                  estimated_time, created_at, is_active) \
          VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?) \
          '''

    try:
        cursor = conn.cursor()

        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º answer_options –≤ JSON —Å—Ç—Ä–æ–∫—É –µ—Å–ª–∏ —ç—Ç–æ —Å–ø–∏—Å–æ–∫
        answer_options = None
        if task_data.get('answer_options'):
            answer_options = json.dumps(task_data['answer_options'], ensure_ascii=False)

        cursor.execute(sql, (
            subject_id,
            exam_type,
            task_data['task_number'],
            task_data['difficulty'],
            task_data['question_text'],
            task_data['answer_type'],
            answer_options,
            task_data['correct_answer'],
            task_data.get('explanation'),
            task_data.get('points', 1),
            task_data.get('estimated_time', 5),
            datetime.now().isoformat(),
            True  # is_active
        ))

        return cursor.lastrowid
    except sqlite3.Error as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ –≤—Å—Ç–∞–≤–∫–∏ –∑–∞–¥–∞–Ω–∏—è: {e}")
        return None


